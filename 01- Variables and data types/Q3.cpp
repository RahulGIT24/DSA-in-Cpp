// Write a program to find length and breadth of a rectangle
#include <iostream>
using namespace std;
int main()
{
    int length, breadth, area;
    length = 20;
    breadth = 90;
    area = length*breadth;
    cout<<"Length of rectangle: "<<length<<endl;
    cout<<"Breadth of rectangle: "<<breadth<<endl;
    cout<<"Area of rectangle: "<<area<<endl;
    return 0;
}